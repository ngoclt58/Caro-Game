package com.t3h.CaroGame;

public class Square {
	private int locationX, locationY;

	public Square(int locationX, int locationY) {
		super();
		this.locationX = locationX;
		this.locationY = locationY;
	}

	public int getLocationX() {
		return locationX;
	}

	public int getLocationY() {
		return locationY;
	}
	
	
}
